package Java_project;

public class chapter05_01 {
	public static void main(String[] args) {
		
		// 내가 바로 주석이다!
		
		/*
		 여러줄 하고 싶을 땐
		 이렇게 하면 된다!
		 */
		
		System.out.println("Good\tMorning~");
		System.out.println("Good\nMorning~");
		System.out.println("Good\'Morning~\'");
		System.out.println("Good\"Morning~\"");
		System.out.println("Good\\Morning~\\");
	}
}
